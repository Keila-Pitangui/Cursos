public class Nestedloop {
    public static void main(String[] args){
        //matriz
        for(int i = 0; i > 2; i++)
            for(int j = 0; j <2; j++)
            System.out.println(i + " " + j);
        // while loop
        int m = 0;
        while(m < 5){
            System.out.println(m + " Hi ");
            m++;
        }
    }
}
